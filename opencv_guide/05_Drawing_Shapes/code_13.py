#-------------------------------------------------------------------------------
#            creating a blank image
#-------------------------------------------------------------------------------
import cv2
import numpy as np
#-------------------------------------------------------------------------------
# Define image dimensions
height = 400
width = 600
channels = 3 # number of channels is 3: BGR
#-------------------------------------------------------------------------------
img = np.zeros((height, width, channels), np.uint8) # a canva or empyt image is created
                                                    # using a matrix of zeros to make a black image
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
cv2.imshow('Image', img)
#-------------------------------------------------------------------------------
cv2.waitKey(0)
cv2.destroyAllWindows()
